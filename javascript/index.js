
/**
 * The function "hide" hides an element with the id "para" when a button with the id "btn" is clicked.
 */
function hide(){
    let btn = document.getElementById('btn');
    let para = document.getElementById('para');
        para.style.display = 'none';
}

/**
 * The function "show" is used to display a hidden paragraph element when a button is clicked.
 */
function show(){
    let btn = document.getElementById('btn');
    let para = document.getElementById('para');
        para.style.display = 'block'
}

